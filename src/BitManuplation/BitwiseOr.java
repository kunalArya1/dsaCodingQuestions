package BitManuplation;

public class BitwiseOr {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println(a | b);

        /*
                OR ( | )

                Any one is "1" then it's output is 1
                    1   0   ->  1
                    0   1   ->  1
                    1   1   ->  1
                    0   0   ->  0

         */
    }
}
