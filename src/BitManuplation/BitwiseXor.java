package BitManuplation;

public class BitwiseXor {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        System.out.println(a ^ b);


        /*
                XOR ( ^ )

                when two input are different then it's output is "1"
                       1    0   ->  1
                       0    1   ->  1

                when two input are same then it's output is "0"
                        1   1   ->  0
                        0   0   ->  0
         */
    }
}
